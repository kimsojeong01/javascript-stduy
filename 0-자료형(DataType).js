/*
자료형(Data type)이란?
다양한 값들을 사용할 수 있다.
*/

/*

01. 숫자 (Number)
숫자형 데이터들은 기본적으로 사직연산이 가능하다.
    정수 (integer)
        -8 -6 -1 //0보다 작은 음의 정수
        0 //0도 포함
        5 10 //0보다 큰 양의 정수

    소수 (floating point)
        3.14 -1.1 1.6 
        과 같은 소수점을 갖는 형태

02. 문자열 (String)
    "hello"    'code'
    큰 따옴표("")나 작은 따옴표('')를 감싸는 형태를 문자열이라고 한다.
    양 끝의 따옴표를 동일하게 해야 한다.
    더하기 기호(+)로 연결이 가능하다.
    
03 불린 (Boolean)
    참(true)과 거짓(false)의 형태
    대체적으로 조건에 의한 결과값으로 사용한다.

*/